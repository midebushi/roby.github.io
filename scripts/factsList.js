class FactsManager {
    constructor(config = {}, data) {
        const {
            parentSelector = '[data-js-facts-list]',
        } = config

        this.data = data

        this.parentElement = document.querySelector(parentSelector)
    }

    init() {
        this.getList()
    }

    getList() {
        this.data.forEach((item) => {
            const svg = item.svg,
                  title = item.title,
                  description = item.description

            this.parentElement.appendChild(this.setItem(svg, title, description))
        });
    }

    setItem(svg, title, description) {
        const newItem = document.createElement('li')
        newItem.classList.add('facts__item')
        newItem.innerHTML = `
            ${svg || ''} 
            <div class="facts__item-info">
                <h3 class="facts__item-title">${title}</h3>
                <p class="facts__item-description">${description}</p>
            </div>
        `

        return newItem
    }
}

const factsManager = new FactsManager({}, data)
factsManager.init()